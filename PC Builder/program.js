function changeQuantity(key, quantity)
{
    let item = document.querySelectorAll(".item");
    if (quantity == 0)
    {
        delete cartContent[key]
        item[key].classList.remove("selected-item")
    }
    else
    {
        cartContent[key].quantity = quantity;
        cartContent[key].price = quantity * parts[key].price;
    }
    refreshCart()
}

function checkout()
{
    let item = document.querySelectorAll(".item")
    let bool = true
    for (i = 0; i < cartList.length; i++)
    {
        if (cartList[i].firstElementChild.classList.contains("cart-item-default"))
        {
            cartList[i].classList.add("red-border")
            bool = false;
        }
    }

    if (bool == true)
    {
        document.getElementById("missing-items-msgbox").style.color = "darkgreen"
        document.getElementById("missing-items-msgbox").innerHTML = "Order sent!"
        for (i = 0; i < cartContent.length; i++)
        {
            delete cartContent[i];
        }

        for (i = 0; i < item.length; i++)
        {
            item[i].classList.remove("selected-item");
        }
        refreshCart();
    }
    else
    {
        document.getElementById("missing-items-msgbox").style.color = "#d1001f"
        document.getElementById("missing-items-msgbox").style.fontSize = "17px"
        document.getElementById("missing-items-msgbox").innerHTML = "Please select one of every item marked with red!"
    }
}

