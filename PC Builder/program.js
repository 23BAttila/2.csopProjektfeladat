function submitRating()
{
    let stars = document.querySelectorAll(".star");

    if (stars[4].classList.contains("star-clicked"))
    {
        listRating(4);
    }
    else if (stars[3].classList.contains("star-clicked"))
    {
        listRating(3);
    }
    else if (stars[2].classList.contains("star-clicked"))
    {
        listRating(2);
    }
    else if (stars[1].classList.contains("star-clicked"))
    {
        listRating(1);
    }
    else if (stars[0].classList.contains("star-clicked"))
    {
        listRating(0);
    }
    else
    {
        alert("Please select a star rating!")
    }
}

function listRating(value)
{
    let ratingContainer = document.getElementById("rating-list-container")
    let ratingText = document.getElementById("rating-text-input")
    let stars = document.querySelectorAll(".star");
    let newRating = document.createElement('div')
    newRating.classList.add("rating")
    
    if (ratingText != "")
    {
        if (value == 0)
        {
            newRating.innerHTML = `
                                <img class="rating-star-count" src="icons/1star.png" alt="One star rating" title="One star rating">
                                <p class="rating-text">${ratingText.value}</p> 
                                `;
        }
        else if (value == 1)
        {
            newRating.innerHTML = `
                                <img class="rating-star-count" src="icons/2stars.png" alt="Two stars rating" title="Two stars rating">
                                <p class="rating-text">${ratingText.value}</p> 
                                `;
        }
        else if (value == 2)
        {
            newRating.innerHTML = `
                                <img class="rating-star-count" src="icons/3stars.png" alt="Three stars rating" title="Three stars rating">
                                <p class="rating-text">${ratingText.value}</p> 
                                `;
        }
        else if (value == 3)
        {
            newRating.innerHTML = `
                                <img class="rating-star-count" src="icons/4stars.png" alt="Four stars rating" title="Four stars rating">
                                <p class="rating-text">${ratingText.value}</p> 
                                `;
        }
        else
        {
            newRating.innerHTML = `
                                <img class="rating-star-count" src="icons/5stars.png" alt="Five stars rating" title="Five stars rating">
                                <p class="rating-text">${ratingText.value}</p> 
                                `;
        }
    }
    else
    {
        alert("Please write something!")
    }

    ratingContainer.append(newRating);
    ratingText.value = "";
    
    for (i = 0; i < stars.length; i++)
    {
        stars[i].classList.remove("star-clicked")
    }
}

